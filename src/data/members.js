// 簡單的 PIN 產生：依卡號生成 4 位數（容易記、可印在實體名牌上）
// 第一個數字 = R1 桌號，後三位是卡號補零
// 例：卡 01 → PIN = 1001  (R1=1, 卡號 001)
//     卡 17 → PIN = 3017  (R1=3, 卡號 017)
//     卡 40 → PIN = 5040  (R1=5, 卡號 040)
function genPin(cardNum) {
  const r1 = CARD_LAYOUT[cardNum - 1][0]
  return `${r1}${String(cardNum).padStart(3, '0')}`
}

// 預設 40 個會員 slot（活動當天填上實際報到名單）
// 目前用 placeholder 名稱，之後可以從報名表匯入
export const MEMBERS = CARD_LAYOUT.map((tables, i) => {
  const cardNum = i + 1
  return {
    id: cardNum,
    cardNum,
    pin: genPin(cardNum),
    name: `會員 #${String(cardNum).padStart(2, '0')}`,  // 之後可改成真實名字
    discordName: '',
    r1: tables[0],
    r2: tables[1],
    r3: tables[2],
  }
})

// 取得 PIN 對應的會員
export function findMemberByPin(pin) {
  return MEMBERS.find(m => m.pin === pin)
}
